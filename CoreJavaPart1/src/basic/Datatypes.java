package basic;

public class Datatypes {
	    static int num;
	    static float numb; 
	    static String str;
	public static void main(String[] args) {
		    System.out.println("Default value of integer is: "+num);
		    System.out.println("Default value of float is: "+numb);
			boolean bool = true;
			System.out.println(bool);
			byte num1; 
			num1 = 108;
			System.out.println(num1);
			short num2; 
			num2 = -258;
			System.out.println(num2);
			int num3 = 102453459; 
			System.out.println(num3);
			long num4 = 50000000000L; 
			System.out.println(num4);
			double num5 = 75.9; 
			System.out.println(num5);
			float num6 = 88.9f; 
			System.out.println(num6);
			char ch1 = '\u0043'; // (Unicode value of C is '\u0041') 
			System.out.println(ch1);
			char ch2 = 67; // ASCII value of 'C' is 65;
			System.out.println(ch2);
			char ch3 = 'C'; 
			System.out.println(ch3);
			String str1="welcome";
			String str2=new String("To Edubridge");
			System.out.println(str1+" "+str2);
			System.out.println("Default value of String is: "+str);
			int arr[]= {1,2,3,4};
			System.out.println("Element at 0st location in the array: "+arr[0]);
			System.out.println("Element at 1st location in the array: "+arr[1]);
			System.out.println("size of array is: "+arr.length);
			String name[]= {"lekha","sai","indu"};
			System.out.println("size of string array is: "+name.length);
			
			
			
			
	}

}
